<?php
class hashingController {

    public final function __construct(){
        global $app;
        $this->app = ($app);

        require_once ("models/plugins/hashier.php");
    }

    /*
     **************************************************************************
     **************************************************************************
                              ***********************
                    ********************************************
     **********               ***********************                **********
    */
    public static function encrypt(string $str)
    {
        if(function_exists('hp5_encode')      )
        { 
            return hp5_encode     ($str)      ;
        }
    }

    /*
     **************************************************************************
     **************************************************************************
                              ***********************
                    ********************************************
     **********               ***********************                **********
    */
    public static function decrypt(string $str)
    {
        if(function_exists('hp5_decode')      )
        { 
            return hp5_decode     ($str)      ;
        }
    }
    /*
     **************************************************************************
     **************************************************************************
                              ***********************
                    ********************************************
     **********               ***********************                **********
    */

}
$hashingController = new hashingController () ;
?>